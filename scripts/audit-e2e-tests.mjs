#!/usr/bin/env node
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const roots = ["tests/e2e", "tests/functional", "tests/regression"];
const outPath = "docs/testing/e2e-audit.md";

function walk(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  return entries.flatMap((entry) => {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) return walk(path);
    if (entry.isFile() && path.endsWith(".test.ts")) return [path];
    return [];
  });
}

function classify(file, content) {
  const lower = `${file}\n${content}`.toLowerCase();
  const assertions = (content.match(/expect\(/g) ?? []).length;
  const screenshots = (content.match(/takeScreenshot/g) ?? []).length;
  const hasOnlyWindowSmoke =
    assertions <= 2 &&
    /getWindowInfo/.test(content) &&
    /isVisible/.test(content) &&
    screenshots > 0;
  const hasCommentScript =
    /\/\/ 1\./.test(content) &&
    /\/\/ 2\./.test(content) &&
    assertions <= 2 &&
    screenshots > 0;

  if (hasCommentScript || hasOnlyWindowSmoke) {
    return ["delete", "Placeholder or screenshot/window-visible smoke without V2 behavioral assertion.", ""];
  }
  if (lower.includes("permission")) {
    return ["delete", "Permission Request live-thread rendering is disabled for current V2.", "Future Permission revival spec"];
  }
  if (
    lower.includes("install-script") ||
    lower.includes("auto update - configuration") ||
    lower.includes("release workflow") ||
    lower.includes("cargo.toml") ||
    lower.includes("tauri.conf.json")
  ) {
    return ["downgrade-unit", "Static configuration/source assertion; not an E2E user journey.", "tests/static or unit test"];
  }
  if (
    lower.includes("p2p") ||
    lower.includes("telemetry") ||
    lower.includes("sync") ||
    lower.includes("notification") ||
    lower.includes("gateway") ||
    lower.includes("channel")
  ) {
    return ["migrate-v2-nightly", "Environment-sensitive integration path; keep out of deterministic PR gate.", "V2 nightly real-chain or controlled integration"];
  }
  if (
    lower.includes("session") ||
    lower.includes("chat") ||
    lower.includes("stream") ||
    lower.includes("tool call") ||
    lower.includes("tool_call")
  ) {
    return ["migrate-v2-pr", "Conversation critical path overlaps the new V2 PR gate.", "tests/v2-e2e/pr/conversation-critical-path.test.ts"];
  }
  if (
    lower.includes("workspace") ||
    lower.includes("file") ||
    lower.includes("diff") ||
    lower.includes("markdown") ||
    lower.includes("terminal")
  ) {
    return ["keep-legacy-manual", "Useful non-conversation coverage, but outside phase-one V2 PR scope.", "Future V2 workspace/file track"];
  }
  if (lower.includes("settings") || lower.includes("knowledge") || lower.includes("layout")) {
    return ["keep-legacy-manual", "Useful product coverage, but outside phase-one V2 PR scope.", "Future V2 settings/layout track"];
  }
  return ["keep-legacy-manual", "Not part of the first V2 conversation gate; preserve manually until reviewed.", ""];
}

const rows = roots.flatMap((root) => walk(root)).sort().map((file) => {
  const content = readFileSync(file, "utf8");
  const currentTrack = file.split("/")[1];
  const [decision, reason, replacement] = classify(file, content);
  return { file, currentTrack, decision, reason, replacement };
});

const body = [
  "# E2E Audit Matrix",
  "",
  "Generated for the V2 E2E rebuild. Decisions are intentionally narrow so the default E2E signal can become trustworthy again.",
  "",
  "| File | Current Track | Decision | Reason | V2 Replacement | Notes |",
  "| --- | --- | --- | --- | --- | --- |",
  ...rows.map((row) =>
    `| \`${row.file}\` | \`${row.currentTrack}\` | \`${row.decision}\` | ${row.reason} | ${row.replacement ? `\`${row.replacement}\`` : ""} | Generated by \`scripts/audit-e2e-tests.mjs\`. |`
  ),
  "",
].join("\n");

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, body);
console.log(`Wrote ${outPath} with ${rows.length} rows`);
